const employee = {
    name: 'Raja',
    designation: 'QA',
    Salary: 20000,
    experience: 1
}

//object.keys used for getting the object keys name
const keys = Object.keys(employee);
console.log(keys);


//object.values used for getting the value of the keys
const values = Object.values(employee);
console.log(values);


//object.entries used of getting all the keys and values
const entries = Object.entries(employee);
console.log(entries);

